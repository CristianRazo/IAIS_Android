package com.andrea.iais2;

public class RecursosGupo {
    private int logo, nombre;

    public RecursosGupo(int logo, int nombre) {
        this.logo = logo;
        this.nombre = nombre;
    }

    public int getLogo() {
        return logo;
    }

    public void setLogo(int logo) {
        this.logo = logo;
    }

    public int getNombre() {
        return nombre;
    }

    public void setNombre(int nombre) {
        this.nombre = nombre;
    }
}
