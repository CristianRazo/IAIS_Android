package com.andrea.iais2;

public class RecursosHijo {
    private String nombre, dato;

    public RecursosHijo(String nombre, String dato) {
        this.nombre = nombre;
        this.dato = dato;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDato() {
        return dato;
    }

    public void setDato(String dato) {
        this.dato = dato;
    }
}
